/**
 * Category schema. Used to organize posts into themes
 * (Real Estate, Historic Restoration, Safety, Heating Season, etc.)
 */
import { defineField, defineType } from "@sanity/types";

export const category = defineType({
  name: "category",
  title: "Category",
  type: "document",
  fields: [
    defineField({
      name: "title",
      title: "Title",
      type: "string",
      validation: (rule) => rule.required().max(60),
    }),
    defineField({
      name: "slug",
      title: "Slug",
      type: "slug",
      options: { source: "title", maxLength: 96 },
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: "description",
      title: "Description",
      type: "text",
      rows: 3,
      validation: (rule) => rule.max(200),
    }),
  ],
});

export default category;
